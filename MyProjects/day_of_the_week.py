day = int(input('Enter a number of the day: '))
if day == 1:
    print('It is Monday')
elif day == 2:
    print('It is Thursday')
elif day == 3:
    print('It is Wendsday')
elif day == 4:
    print('It is Tuesday')
elif day == 5:
    print('It is Friday')
elif day == 6:
    print('It is Saturday')
elif day == 7:
    print('It is Sunday')
else:
    print('Error. Enter the correct number from 1 to 7')
